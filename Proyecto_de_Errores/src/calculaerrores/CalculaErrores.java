    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
    try {
        // Se obtienen los valores que el usuario escribio y se convierten en numeros
        double valorReal = Double.parseDouble(txtValorReal.getText());
        double valorAprox = Double.parseDouble(txtValorAprox.getText());
        
        // calculamos el error absoluto
        double errorAbs = Math.abs(valorReal - valorAprox);
        //  calculamos el error relativo en base al valor real
        double errorRel = errorAbs / Math.abs(valorReal);

        // Formateamos resultados paarq ue tengan dos decimales 
        DecimalFormat df = new DecimalFormat("#.##"); // 2 decimales
        txtErrorAbs.setText(df.format(errorAbs));
        txtErrorRel.setText(df.format(errorRel * 100) + " %"); // se multiplicaÂ´por 100 para el porcentaje
        
    } catch (NumberFormatException e) {
         // Si el usuario escribio letras o algo invalido, se muestra un mensaje de error
        JOptionPane.showMessageDialog(this, 
                "Por favor ingresa valores numericos validos", 
                "Error de entrada", 
                JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnCalcularActionPerformed

